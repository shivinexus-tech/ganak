function sunGeo(JDE) {
  const T = (JDE - 2451545) / 36525, tau = T / 10;
  const ser = (tab) => tab.reduce((s, [a, b, c]) => s + a * Math.cos(b + c * tau), 0);
  const L = ser(EARTH_L0) + ser(EARTH_L1) * tau + ser(EARTH_L2) * tau * tau + ser(EARTH_L3) * tau ** 3;
  const R = (ser(EARTH_R0) + ser(EARTH_R1) * tau) / 1e8; // AU
  const Learth = rev((L / 1e8) / D2R);
  let lon = rev(Learth + 180);                     // Sun geometric longitude, mean equinox of date
  // FK5 conversion (VSOP→FK5): Δλ = -0.09033" + correction; for the Sun the standard offset:
  lon = rev(lon - 0.09033 / 3600);
  // aberration: -20.4898" / R  (distance-dependent), plus nutation in longitude
  const dpsi = nutationLon(T);
  lon = rev(lon - (20.4898 / 3600) / R + dpsi);
  return { lon, R };
}

/* bridge to the app's "days since 2451543.5" convention; applies ΔT to reach JDE */
function jdeFromD(d) {
  const JD = d + 2451543.5;
  const y = 2000 + (JD - 2451545.0) / 365.25;
  return JD + deltaTsec(y) / 86400;
}

function sunPos(d) {
  const g = sunGeo(jdeFromD(d));
  return { lon: g.lon, r: g.R };
}

function keplerE(M, e) {
  let E = M + e * (180 / Math.PI) * sd(M) * (1 + e * cdg(M));
  for (let i = 0; i < 6; i++) {
    E = E - (E - (180 / Math.PI) * e * sd(E) - M) / (1 - e * cdg(E));
  }
  return E;
}

